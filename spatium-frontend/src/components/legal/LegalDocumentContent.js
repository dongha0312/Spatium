export function LegalDocumentContent({ sections, compact = false }) {
  return (
    <>
      {sections.map((section) => (
        <section
          className={compact ? "su-legal-section" : "cp-section"}
          key={section.title}
        >
          {compact ? (
            <h3>{section.title}</h3>
          ) : (
            <div className="cp-section-title">{section.title}</div>
          )}
          {section.paragraphs?.map((paragraph) => (
            <p className={compact ? undefined : "cp-text"} key={paragraph}>
              {paragraph}
            </p>
          ))}
          {section.bullets && (
            <ul className={compact ? undefined : "cp-list"}>
              {section.bullets.map((item) => (
                <li key={item}>{item}</li>
              ))}
            </ul>
          )}
          {section.note && (
            <div className={compact ? "su-legal-note" : "cp-note"}>
              {section.note}
            </div>
          )}
        </section>
      ))}
    </>
  );
}
